def main():
    # Load existing
    with open(KB_FILE, 'r', encoding='utf-8') as f:
        kb = json.load(f)

    with open(TPL_FILE, 'r', encoding='utf-8') as f:
        tpl = json.load(f)

    orig_categories = len(kb['categories'])
    orig_scenarios = sum(len(c['scenarios']) for c in kb['categories'].values())
    orig_faqs = len(kb['faqs'])
    orig_tpls = len(tpl)

    # 1. Add new categories
    for cat_id, cat_data in NEW_CATEGORIES.items():
        kb['categories'][cat_id] = cat_data
        print(f'新增类别: {cat_data["name"]} ({len(cat_data["scenarios"])} 个场景)')

    # 2. Expand existing categories
    for cat_id, scenarios in EXPAND_SCENARIOS.items():
        if cat_id in kb['categories']:
            for scn_id, scn_data in scenarios.items():
                kb['categories'][cat_id]['scenarios'][scn_id] = scn_data
            print(f'扩充类别: {kb["categories"][cat_id]["name"]} (+{len(scenarios)} 场景)')

    # 3. Add new templates
    existing_tpl_ids = {t['id'] for t in tpl}
    for tpl_data in NEW_TEMPLATES:
        if tpl_data['id'] not in existing_tpl_ids:
            tpl.append(tpl_data)
            print(f'新增模板: {tpl_data["name"]}')

    # 4. Add new FAQs
    kb['faqs'].extend(NEW_FAQS)
    print(f'新增FAQ: {len(NEW_FAQS)} 条')

    # Save
    with open(KB_FILE, 'w', encoding='utf-8') as f:
        json.dump(kb, f, ensure_ascii=False, indent=2)

    with open(TPL_FILE, 'w', encoding='utf-8') as f:
        json.dump(tpl, f, ensure_ascii=False, indent=2)

    # Stats
    new_categories = len(kb['categories']) - orig_categories
    new_scenarios = sum(len(c['scenarios']) for c in kb['categories'].values()) - orig_scenarios
    new_faqs = len(kb['faqs']) - orig_faqs
    new_tpls = len(tpl) - orig_tpls

    print(f'\n{"=" * 50}')
    print(f'  扩展完成!')
    print(f'  类别: {orig_categories} → {len(kb["categories"])} (+{new_categories})')
    print(f'  场景: {orig_scenarios} → {sum(len(c["scenarios"]) for c in kb["categories"].values())} (+{new_scenarios})')
    print(f'  FAQ: {orig_faqs} → {len(kb["faqs"])} (+{new_faqs})')
    print(f'  模板: {orig_tpls} → {len(tpl)} (+{new_tpls})')
    print(f'{"=" * 50}')

    # Print all categories
    for c in kb['categories'].values():
        print(f'  {c["icon"]} {c["name"]}: {len(c["scenarios"])} 个场景')


if __name__ == '__main__':
    main()
